#!/usr/bin/env python3
"""
Module provides function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and returns the result.
    Args:
        str1 (str): first string.
        str2 (str): second string.
    Returns:
        str: concatenated string.
    """
    return str1 + str2
